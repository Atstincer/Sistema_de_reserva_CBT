    private void Filtrar(){
        String buscar = jTextField_buscar.getText();
        String campo = jComboBox_campos.getSelectedItem().toString();
        String query = "";
        
        if(!campo.equals("Todos") && buscar.equals("")){
            JOptionPane.showMessageDialog(null,"Debe proporcionar la informaciÃ³n a buscar.");
            return;
        }
        
        if(campo.equals("Todos")){
            jTextField_buscar.setText("");
            query = "select Id_servicio, Descripcion, TE_alojamiento, Reservado_fecha, Reservado_por, TE_traslado "
                    + "from servicios where Descripcion = 'CANCELADO' and CXX = 'SI'";
        } else if(campo.equals("TE")){
            query = "select Id_servicio, Descripcion, TE_alojamiento, Reservado_fecha, Reservado_por, TE_traslado "
                    + "from servicios where Descripcion = 'CANCELADO' and CXX = 'SI' and "
                    + "(TE_alojamiento = '" + buscar + "' or TE_traslado = '" + buscar + "')";
        }else if(campo.equals("Fecha_reservado")){
            if(Util.ValidDate(buscar)){
                query = "select Id_servicio, Descripcion, TE_alojamiento, Reservado_fecha, Reservado_por, TE_traslado "
                        + "from servicios where Descripcion = 'CANCELADO' and CXX = 'SI' and "
                        + "Reservado_fecha = '" + buscar + "'";
            }else{
                JOptionPane.showMessageDialog(null,"Lo sentimos. La operaciÃ³n no se puede realizar.\n"
                        + "Debe introducir la fecha deseada en el formato 'dia/mes/aÃ±o'.");
                return;
            }            
        }
        
        Util.VaciarTabla(model);
        
        try{
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(query);
            
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                Object[] fila_rs = new Object[6];
                Object[] fila = new Object[5];
                
                for(int i=0; i<fila_rs.length; i++){//llenando fila_rs
                    fila_rs[i] = rs.getString(i+1);
                }
                
                if(!fila_rs[2].equals("")){//si TE_alojamiento
                    for(int i=0; i<fila.length; i++){
                        fila[i] = fila_rs[i];
                    }
                    model.addRow(fila);
                }
                
                if(!fila_rs[5].equals("")){//si TE_traslado
                    for(int i=0; i<fila.length; i++){
                        fila[i] = fila_rs[i];
                    }
                    fila[2] = fila_rs[5];
                    model.addRow(fila);
                }                
            }
            cn.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Error: " + e.getMessage().toString());
        }        
    }  

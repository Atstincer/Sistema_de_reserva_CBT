    public void Filtrar() {
        int campo = jComboBox_campos.getSelectedIndex();
        String abuscar = jTextField_buscar.getText().trim();
        String query = "";

        if (campo == 0) {//filtrar por todos
            query = "select Id_cliente, Nombre, Apellido, Nacionalidad, Telefono_fijo, "
                    + "Telefono_cell, E_mail, Observaciones from Clientes";
            jTextField_buscar.setText("");
            abuscar = "abc";
        } else if (campo == 1) {//filtrar por nombre
            query = "select Id_cliente, Nombre, Apellido, Nacionalidad, Telefono_fijo, "
                    + "Telefono_cell, E_mail, Observaciones from Clientes where Nombre = '" + abuscar + "'";
        } else if (campo == 2) {//filtrar por telÃ©fono fijo
            query = "select Id_cliente, Nombre, Apellido, Nacionalidad, Telefono_fijo, "
                    + "Telefono_cell, E_mail, Observaciones from Clientes where Telefono_fijo = '" + abuscar + "'";
        } else if (campo == 3) {//filtrar por cell
            query = "select Id_cliente, Nombre, Apellido, Nacionalidad, Telefono_fijo, "
                    + "Telefono_cell, E_mail, Observaciones from Clientes where Telefono_cell = '" + abuscar + "'";
        } else if (campo == 4) {//filtrar por email
            query = "select Id_cliente, Nombre, Apellido, Nacionalidad, Telefono_fijo, "
                    + "Telefono_cell, E_mail, Observaciones from Clientes where E_mail = '" + abuscar + "'";
        }

        if (!abuscar.equals("")) {
            VaciarTabla();
            try {
                Connection cn2 = Conexion.conectar();
                PreparedStatement pst = cn2.prepareStatement(query);

                ResultSet rs = pst.executeQuery();

                while (rs.next()) {
                    Object[] fila = new Object[8];

                    for (int i = 0; i < 8; i++) {
                        fila[i] = rs.getObject(i + 1);
                    }
                    mode1.addRow(fila);
                }
                cn2.close();
            } catch (SQLException e) {
                System.err.println("Error al filtrar: " + e);
                JOptionPane.showMessageDialog(null, "!!Error al filtrar...comunÃ­quese con el administrador");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debes ingresar el elemento que deseas buscar.");
        }
    }


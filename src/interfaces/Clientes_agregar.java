    public void Limpiar() {
        jTextField_nombre.setText("");
        jTextField_apellido.setText("");
        jTextField_nacionalidad.setText("");
        jTextField_telefono.setText("");
        jTextField_cell.setText("");
        jTextField_mail.setText("");
        jTextArea_observaciones.setText("");
    }
    
    public void Agregar(){
         String nombre = jTextField_nombre.getText().trim();
        String apellido = jTextField_apellido.getText().trim();
        String telefono = jTextField_telefono.getText().trim();
        String cell = jTextField_cell.getText().trim();
        String nacionalidad = jTextField_nacionalidad.getText().trim();
        String mail = jTextField_mail.getText().trim();
        String observaciones = jTextArea_observaciones.getText().trim();

        if (!nombre.equals("") && !apellido.equals("")) {
            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement("insert into Clientes values (?,?,?,?,?,?,?,?)");

                pst.setInt(1, 0);
                pst.setString(2, nombre);
                pst.setString(3, apellido);
                pst.setString(4, nacionalidad);
                pst.setString(5, telefono);
                pst.setString(6, cell);
                pst.setString(7, observaciones);
                pst.setString(8, mail);

                pst.executeUpdate();
                cn.close();

                Limpiar();

                jTextField_nombre.setBackground(Color.green);
                jTextField_apellido.setBackground(Color.green);
                jTextField_nacionalidad.setBackground(Color.green);
                jTextField_telefono.setBackground(Color.green);
                jTextField_cell.setBackground(Color.green);
                jTextField_mail.setBackground(Color.green);
                jTextArea_observaciones.setBackground(Color.green);

                JOptionPane.showMessageDialog(null, "Los datos se han ingresado correctamente");
                this.dispose();

            } catch (SQLException e) {
                System.err.println("Error en registrar cliente. " + e);
                JOptionPane.showMessageDialog(null, "!!Error al registrar cliente...contacte a su administrador.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debes llenar los campos nombre y apellido.");
        }
    }
    
